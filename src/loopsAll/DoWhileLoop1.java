package loopsAll;

public class DoWhileLoop1 {
    public static void main(String[] args) {
        int i = 0;
        do {
            System.out.println("Practicing more do while loop");
            i++;
        } while (i < 3);

        int j = 0;
        do {
            System.out.println("Decremental do while loop");
            j--;
        } while (j > -3);

    }

}
