package loopsAll;

public class Forloop1 {
    public static void main(String[] args) {
        for (int i = 0; i < 3; i++) {
            System.out.println("More Practice more confidence " + i);
        }
        for (int j = 0; j > -2; j--) {
            System.out.println("This is decremental loop " + j);
        }

    }
}
