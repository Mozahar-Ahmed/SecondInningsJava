package loopsAll;

public class WhileLoop1 {
    public static void main(String[] args) {
        int i = 0;
        while (i < 5) {
            System.out.println("Practicing more while loop " + i);
            i++;
        }
        int j = 0;
        while (j > -3) {
            System.out.println("Its decremental while loop " + j);
            j--;

        }

    }
}
