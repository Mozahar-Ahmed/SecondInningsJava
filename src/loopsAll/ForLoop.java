package loopsAll;

public class ForLoop {
    public static void main(String[] args) {
        String name = "Mozahar ";
        for (int a = 0; a < 10; a++) {
            System.out.println(name + a);
        }
        System.out.println(".........Border line........");

        for (int i = 0; i > -5; i--) {
            System.out.println(name + i);
        }
    }
}
